def get_params(vocab_size, num_hiddens, device):#初始化参数使用的


    num_inputs = num_outputs = vocab_size

    def normal(shape):
        return torch.randn(size=shape, device=device) * 0.01

    # 隐藏层参数
    W_xh = normal((num_inputs, num_hiddens))#(28,512)#初始化参数
    W_hh = normal((num_hiddens, num_hiddens))#(512,512)
    b_h = torch.zeros(num_hiddens, device=device)#(512,)
    # 输出层参数
    W_hq = normal((num_hiddens, num_outputs))#(512,28)
    b_q = torch.zeros(num_outputs, device=device)#(28,)
    # 附加梯度
    params = [W_xh, W_hh, b_h, W_hq, b_q]#[(28,512),(512,512),(512,),(512,28),(28,)]
    for param in params:
        param.requires_grad_(True)
    return params


